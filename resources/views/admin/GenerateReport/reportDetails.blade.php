function printReport() {
    const printable = document.getElementById('printableArea').innerHTML;
    const styles = `
        <style>
            @page { size: A4; margin: 20mm; }
            body { font-family: 'Inter', Arial, sans-serif; color: #111; }
            table { border-collapse: collapse; width: 100%; }
            th { text-align: left; padding: 8px; border-bottom: 1px solid #ddd; font-weight:700; }
            td { padding: 8px; vertical-align: top; }
        </style>
    `;

    const html = '<!doctype html><html><head><meta charset="utf-8"><title>Report #{{ $report->reportID }}</title>' + styles + '</head><body>' + printable + '</body></html>';

    // Open new window
    const w = window.open('', '_blank', 'width=900,height=700');
    if (!w) {
        alert('Please allow popups for this site to print reports.');
        return;
    }

    // Write content to new window
    w.document.open();
    w.document.write(html);
    w.document.close();
    w.focus();

    // Wait for content to load, then print
    setTimeout(() => {
        try { w.print(); } catch (e) { console.error('Print error:', e); }
    }, 300);
}
// Auto-trigger print when ?print=1 is present
document.addEventListener('DOMContentLoaded', function() {
    try {
        const params = new URLSearchParams(window.location.search);
        if (params.get('print') === '1') {
            // give the page a moment to render
            setTimeout(() => { if (typeof printReport === 'function') printReport(); }, 500);
        }
    } catch (e) { /* ignore */ }
});

/**
 * Scans a table and hides any column where EVERY row contains "N/A"
 */
function hideEmptyColumns() {
    const tables = document.querySelectorAll('.report-table');
    
    tables.forEach(table => {
        const rows = table.querySelectorAll('tbody tr');
        if (rows.length === 0 || (rows.length === 1 && rows[0].cells.length === 1)) return;

        const colCount = table.querySelectorAll('thead th').length;

        for (let i = 0; i < colCount; i++) {
            let allNA = true;
            let hasData = false;

            rows.forEach(row => {
                const cell = row.cells[i];
                if (cell) {
                    hasData = true;
                    const text = cell.textContent.trim();
                    // If even one row has something other than N/A, we show the column
                    if (text !== 'N/A' && text !== '') {
                        allNA = false;
                    }
                }
            });

            if (allNA && hasData) {
                // Hide header
                table.querySelectorAll('thead th')[i].classList.add('column-hidden');
                // Hide all cells in this column
                rows.forEach(row => {
                    if (row.cells[i]) row.cells[i].classList.add('column-hidden');
                });
            }
        }
    });
}

document.addEventListener('DOMContentLoaded', hideEmptyColumns);
